
public class DatatypesDemo {
	
	public static void main(String[] args) {
		
		byte numberOfMembersInFamily = 6;
		System.out.println("Number of family members is " + numberOfMembersInFamily);
	
		short noOfLearnersInJavaFSDSession = 250;
		System.out.println("Number of Learners is " + noOfLearnersInJavaFSDSession);
	
		int numberOfEmployeesInABCCorp = 280000; 
		System.out.println("Number of employees in ABC Corp is " + numberOfEmployeesInABCCorp);
		
		long populationInIndia = 1417173173;
		System.out.println("India's Population is " + populationInIndia);
		
		long populationCountInEarth = 100000000000000L;
		System.out.println("Earth's Population is " + populationCountInEarth);
	
		float f = 10.0F;
		System.out.println("Float f is " + f);
				
		float f2 = 10.1f;	
		System.out.println("Float f2 is " + f2);

		double d1 = 3500000.544;
		System.out.println("Double d1 is " + d1);

		double d2 = 1500.23;
		System.out.println("Double d2 is " + d2);

		char initial = 'B';
		System.out.println("Initial is " +initial);

		boolean b = true;
		System.out.println("Boolean value is " + b);
		
		b = false;
		System.out.println("Boolean value is " + b);
		

  }

}

