
public class ConditionalExecutionFlow {

	public static void main(String[] args) {
				
		
		int i = 10;		
		System.out.println("I's value is ->" + i);		

		if (i == 10) {

			int j = 20;		
			System.out.println("J's value is ->" + j);
			
		}else {

			int k = 30;
			System.out.println("K's value is ->" + k);
			
		}
		
	}
}
